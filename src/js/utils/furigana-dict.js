/**
 * 漢字→読み置換（長文優先）
 */
export function applyDictionary(text, customDict = {}) {
  if (!text) return text;
  
  // ユーザー辞書と内蔵辞書をマージ
  const mergedDict = { ...FURIGANA_DICT, ...customDict };
  
  let result = text;
  // キーの長さ順（降順）に並べ替え
  const keys = Object.keys(mergedDict).sort((a, b) => b.length - a.length);
  
  for (const key of keys) {
    if (result.includes(key)) {
      // 置換対象が既にカタカナ（読み）の場合はスキップ
      // (ただし、この簡易ロジックでは完全な衝突回避は難しいため、
      // 実際には一気に全置換するか、より厳密なトークン管理が必要)
      result = result.split(key).join(mergedDict[key]);
    }
  }
  return result;
}
