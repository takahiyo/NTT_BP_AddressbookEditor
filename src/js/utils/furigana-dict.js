/**
 * テキストから辞書にマッチする漢字を抽出してカタカナ読みに置換
 * @param {string} text - 対象テキスト
 * @returns {string} 読み変換後のテキスト
 */
export function applyDictionary(text) {
  if (!text) return '';
  let result = text;
  
  // 文字列長が長い順にソートして置換（「株式会社」が「株式」より先にマッチするように）
  const keys = Object.keys(FURIGANA_DICT).sort((a, b) => b.length - a.length);
  
  for (const key of keys) {
    if (result.includes(key)) {
      result = result.split(key).join(FURIGANA_DICT[key]);
    }
  }
  return result;
}
